/**
 * @fileoverview E2E tests for chunk generation, embedding, and OpenSearch indexing.
 *
 * Verifies the full pipeline after document parsing: chunks exist in OpenSearch
 * with correct content, embeddings (q_vec), and metadata (kb_id, doc_id).
 * Also validates that chunks are searchable and that the chunk count in
 * OpenSearch matches the document metadata (chunk_num).
 *
 * Uses direct OpenSearch queries via opensearch.helper.ts to verify indexing
 * at the storage layer, complementing API-level assertions.
 *
 * Prerequisites:
 * - All infrastructure running: `npm run docker:base && npm run dev`
 * - Auth setup has run (Playwright handles this via project dependencies)
 * - Embedding model configured (advance-rag DEFAULT_EMBEDDING_MODEL)
 *
 * Tags:
 * - @smoke: Critical path test for chunk existence after parsing
 *
 * @module e2e/dataset/chunk-verify.spec
 */

import path from 'path'
import { fileURLToPath } from 'url'
import { test, expect, APIRequestContext } from '@playwright/test'
import { ApiHelper, apiHelper } from '../helpers/api.helper'
import { waitForDocumentParsed, waitForChunksIndexed } from '../helpers/wait.helper'
import { OpenSearchHelper, opensearchHelper, ChunkResult } from '../helpers/opensearch.helper'

/** ESM-compatible __dirname equivalent */
const __dirname = path.dirname(fileURLToPath(import.meta.url))

/** Path to test PDF file */
const SAMPLE_PDF_PATH = path.resolve(__dirname, '../test-data/sample.pdf')

/** Base URL for direct backend API calls */
const API_BASE = process.env.E2E_API_BASE || 'http://localhost:3001'

/** API helper instance for setup/teardown */
let api: ApiHelper

/** OpenSearch helper for direct index queries */
let osHelper: OpenSearchHelper

/** Shared dataset created for chunk verification tests */
let datasetId: string

/** Document uploaded and parsed for chunk verification */
let docId: string

/** Chunks retrieved from OpenSearch (cached for multiple test assertions) */
let chunks: ChunkResult[]

test.beforeAll(async ({ request }) => {
  // Create helpers
  api = apiHelper(request)
  osHelper = opensearchHelper()

  // Create a test dataset with the default naive parser
  const dataset = await api.createDataset(
    `E2E Chunk Verify ${Date.now()}`,
    'Dataset for chunk/embedding/indexing verification'
  )
  datasetId = dataset.id

  // Upload sample.pdf for parsing
  const docs = await api.uploadDocument(datasetId, SAMPLE_PDF_PATH)
  docId = docs[0].id

  // Trigger parsing via API
  await api.triggerParse(datasetId, [docId])

  // Wait for parsing to complete (up to 120s for slow embedding models)
  await waitForDocumentParsed(request, datasetId, docId, 120_000)

  // Wait for chunks to appear via the API (confirms backend sees them)
  await waitForChunksIndexed(request, datasetId, docId, 1, 60_000)

  // Refresh the OpenSearch index to ensure all chunks are immediately searchable
  const indexName = osHelper.getIndexName()
  await osHelper.refreshIndex(indexName)

  // Fetch chunks from OpenSearch for use in all test assertions
  chunks = await osHelper.getChunksByDocId(indexName, docId)
})

test.afterAll(async () => {
  // Clean up the test dataset (cascading deletes documents and chunks)
  try {
    await api.deleteDataset(datasetId)
  } catch {
    // Ignore cleanup errors -- dataset may already be deleted
  }
})

// ============================================================================
// Chunk existence after parsing
// ============================================================================

test('Chunks exist in OpenSearch after parsing @smoke', async () => {
  // At least 1 chunk must exist after parsing a non-empty PDF
  expect(chunks.length).toBeGreaterThan(0)

  for (const chunk of chunks) {
    // Each chunk must have non-empty text content
    expect(chunk.content.length).toBeGreaterThan(0)

    // Each chunk's kb_id must match the dataset ID (32-char hex, no hyphens)
    const normalizedDatasetId = osHelper.normalizeUuid(datasetId)
    expect(chunk.kb_id).toBe(normalizedDatasetId)

    // Each chunk's doc_id must match the document ID (32-char hex, no hyphens)
    const normalizedDocId = osHelper.normalizeUuid(docId)
    expect(chunk.doc_id).toBe(normalizedDocId)
  }
})

// ============================================================================
// Chunk embeddings (q_vec)
// ============================================================================

test('Chunks have embedding vectors (q_vec)', async () => {
  // Skip if no chunks were found (previous test would have caught this)
  expect(chunks.length).toBeGreaterThan(0)

  for (const chunk of chunks) {
    // Each chunk should have an embedding vector generated by the embedding model
    expect(chunk.has_embedding).toBe(true)

    // Embedding dimension should be > 0 (varies by model: 768, 1024, 1536, etc.)
    expect(chunk.embedding_dim).toBeGreaterThan(0)
  }
})

// ============================================================================
// Chunk content matches source document
// ============================================================================

test('Chunk content_with_weight contains text from source document', async () => {
  expect(chunks.length).toBeGreaterThan(0)

  // Collect all chunk text into a single string for flexible matching
  const allChunkText = chunks.map((c) => c.content.toLowerCase()).join(' ')

  // The sample.pdf should contain some recognizable text -- verify at least
  // one chunk has substantial content (not just whitespace or metadata)
  const hasSubstantiveContent = chunks.some(
    (c) => c.content.trim().length > 10
  )
  expect(hasSubstantiveContent).toBe(true)

  // Verify the combined chunk text has reasonable total length
  // (a real PDF should produce more than a few characters of text)
  expect(allChunkText.length).toBeGreaterThan(20)
})

// ============================================================================
// Chunks are searchable via dataset search API
// ============================================================================

test('Chunks are searchable via dataset search API', async ({ request }) => {
  // Get a snippet from the first chunk to use as a search query
  const sampleText = chunks[0]?.content || ''
  // Use the first few meaningful words as a search query
  const searchTerms = sampleText
    .replace(/[^\w\s]/g, ' ')
    .split(/\s+/)
    .filter((w) => w.length > 3)
    .slice(0, 3)
    .join(' ')

  // Skip if we cannot extract meaningful search terms from chunk content
  if (!searchTerms.trim()) {
    test.skip()
    return
  }

  // Search via the backend API endpoint
  const response = await request.post(
    `${API_BASE}/api/rag/datasets/${datasetId}/search`,
    {
      data: {
        query: searchTerms,
        method: 'full_text',
        top_k: 5,
        similarity_threshold: 0,
      },
    }
  )

  expect(response.ok()).toBe(true)

  const json = await response.json()
  const results = json.chunks || []

  // At least one search result should be returned
  expect(results.length).toBeGreaterThan(0)

  // Verify the result includes chunk content and document reference
  const firstResult = results[0]
  expect(firstResult.text || firstResult.content_with_weight).toBeTruthy()
  expect(firstResult.doc_id).toBeTruthy()
})

// ============================================================================
// Chunk count matches document metadata
// ============================================================================

test('Chunk count in OpenSearch matches document chunk_num metadata', async ({ request }) => {
  // Get chunk_num from document metadata via the API
  const docResponse = await request.get(
    `${API_BASE}/api/rag/datasets/${datasetId}/documents`
  )
  expect(docResponse.ok()).toBe(true)

  const docJson = await docResponse.json()
  const doc = docJson.data?.find((d: { id: string }) => d.id === docId)
  expect(doc).toBeTruthy()

  // chunk_num is the RAGFlow field tracking how many chunks were generated
  const metadataChunkNum = doc.chunk_num ?? doc.chunk_count ?? 0

  // Get actual chunk count from OpenSearch
  const osChunkCount = chunks.length

  // The metadata chunk_num should match the actual number of chunks in OpenSearch
  // Allow exact match -- if these differ, there is an indexing consistency bug
  expect(osChunkCount).toBe(metadataChunkNum)
})
